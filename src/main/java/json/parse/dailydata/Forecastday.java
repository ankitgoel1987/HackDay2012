package json.parse.dailydata;

public class Forecastday {
	private String _period;
	private String _icon;
	private String _icon_url;
	private String _title;
	private String _fcttext;
	private String _fcttext_metric;
	private String _pop;
	
	public String getPeriod() {
		return _period;
	}
	public void setPeriod(String _period) {
		this._period = _period;
	}
	public String getIcon() {
		return _icon;
	}
	public void setIcon(String _icon) {
		this._icon = _icon;
	}
	public String getIcon_url() {
		return _icon_url;
	}
	public void setIcon_url(String _icon_url) {
		this._icon_url = _icon_url;
	}
	public String getTitle() {
		return _title;
	}
	public void setTitle(String _title) {
		this._title = _title;
	}
	public String getFcttext() {
		return _fcttext;
	}
	public void setFcttext(String _fcttext) {
		this._fcttext = _fcttext;
	}
	public String getFcttext_metric() {
		return _fcttext_metric;
	}
	public void setFcttext_metric(String _fcttext_metric) {
		this._fcttext_metric = _fcttext_metric;
	}
	public String getPop() {
		return _pop;
	}
	public void setPop(String _pop) {
		this._pop = _pop;
	}
}
 